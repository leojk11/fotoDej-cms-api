const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const clientSchema = new Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    phone_number: {
        type: String
    },

    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },

    profile_image: {
        type: String
    },

    number_of_albums: {
        type: Number,
        required: true
    }
});

const Client = mongoose.model('Client', clientSchema);
module.exports = Client;